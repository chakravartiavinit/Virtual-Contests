int main()
{
#ifndef ONLINE_JUDGE
    // freopen("error.txt", "w", stderr);
#endif
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int n;
    cin >> n;
    vector<long long> vt(n);
    for (int i = 0; i < n; ++i)
    {
        cin >> vt[i];
    }
    if (n <= 2)
    {
        cout << "0\n";
    }
    else
    {
        vector<int> dir = {-1, 0, 1};
        vector<long long> common_differences;
        int start = 0;
        vector<vector<long long>> all_temps(9);
        map<int, int> mp;
        for (int k = 0; k < 3; ++k)
        {
            for (int l = 0; l < 3; ++l)
            {
                vector<long long> temp;
                temp = vt;
                long long temp1 = (temp[0] + dir[k]) - (temp[1] + dir[l]);
                temp[0] += dir[k];
                temp[1] += dir[l];
                if (dir[k] != 0)
                {
                    mp[start]++;
                }
                if (dir[l] != 0)
                {
                    mp[start]++;
                }
                common_differences.push_back(temp1);
                all_temps[start] = temp;
                start++;
            }
        }
        int answer = INT_MAX;
        for (int i = 0; i < common_differences.size(); ++i)
        {
            vector<long long> temp = all_temps[i];
            long long diff = common_differences[i];
            int count = mp[i];
            bool flag = true;
            for (int i = 2; i < n; ++i)
            {
                long long temp_diff = temp[i - 1] - temp[i];
                if (abs(temp_diff - diff) > 1)
                {
                    flag = false;
                }
                count += abs(temp_diff - diff);
                temp[i] = temp[i - 1] - diff;
            }
            // debug(temp);
            if (flag == true)
            {
                answer = min(answer, count);
            }
        }
        if (answer == INT_MAX)
        {
            cout << "-1\n";
        }
        else
        {
            cout << answer << "\n";
        }
    }
}